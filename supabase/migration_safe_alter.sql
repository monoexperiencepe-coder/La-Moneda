-- ---------------------------------------------------------------------------
-- pendientes (nueva tabla)
-- ---------------------------------------------------------------------------
create table if not exists public.pendientes (
  id bigint generated by default as identity primary key,
  empresa_id uuid not null references public.empresas (id) on delete cascade,
  vehicle_id bigint references public.vehiculos (id) on delete set null,
  descripcion text not null default '',
  estado text not null default 'ABIERTO',
  fecha date not null default (current_date),
  prioridad text not null default 'MEDIA',
  created_at timestamptz not null default now()
);

create index if not exists pendientes_empresa_id_idx on public.pendientes (empresa_id);
create index if not exists pendientes_empresa_fecha_idx on public.pendientes (empresa_id, fecha desc);

-- ---------------------------------------------------------------------------
-- registros_tiempo (hoja TIEMPO Excel)
-- ---------------------------------------------------------------------------
create table if not exists public.registros_tiempo (
  id bigint generated by default as identity primary key,
  empresa_id uuid not null references public.empresas (id) on delete cascade,
  vehicle_id bigint references public.vehiculos (id) on delete set null,
  fecha_registro timestamptz not null default now(),
  fecha date not null default (current_date),
  detalle_auto text,
  tipo text,
  descripcion text,
  valor_tiempo numeric,
  created_at timestamptz not null default now()
);

create index if not exists registros_tiempo_empresa_id_idx on public.registros_tiempo (empresa_id);
create index if not exists registros_tiempo_empresa_fecha_idx on public.registros_tiempo (empresa_id, fecha desc);
create index if not exists registros_tiempo_vehicle_id_idx on public.registros_tiempo (vehicle_id);

