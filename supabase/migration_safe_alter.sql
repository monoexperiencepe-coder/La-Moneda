-- migration_safe_alter.sql
-- Alinea tablas existentes con schema_reference.sql SIN borrar datos.
-- Reglas: sin DROP TABLE, sin DROP COLUMN, solo ADD COLUMN IF NOT EXISTS.
-- empresa_id permanece uuid. RLS no se toca aquí.
--
-- AVISOS MANUALES (revisar antes / después de ejecutar):
-- 1) Si una columna homónima ya existe con tipo incompatible (p. ej. fecha como text,
--    monto como double precision vs numeric, vehicle_id integer vs bigint), esta
--    migración NO la cambia: debes ALTER COLUMN ... TYPE ... USING ... en un script aparte.
-- 2) ADD COLUMN ... NOT NULL sin DEFAULT falla si la tabla tiene filas: aquí se usan
--    DEFAULTs seguros o columnas nullable donde hace falta; luego puedes endurecer NOT NULL.
-- 3) Columnas nuevas nullable (p. ej. empresa_id, fecha en tablas con datos): las filas
--    existentes quedan NULL hasta que ejecutes UPDATE; luego aplica NOT NULL si lo necesitas.
-- 4) Si tus tablas antiguas usan camelCase (vehicleId, fechaRegistro), los nombres no
--    coinciden con la app: renombra o crea vistas/columnas generadas aparte; este script
--    solo añade nombres snake_case del schema_reference.
-- 5) Claves foráneas nuevas no se añaden (riesgo con datos huérfanos). Tras validar
--    vehicle_id y empresa_id, crea FKs en otro paso.

